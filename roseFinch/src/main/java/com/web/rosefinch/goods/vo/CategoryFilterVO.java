package com.web.rosefinch.goods.vo;

public class CategoryFilterVO {
	private int catCode;
	private String catName;
	private int catGroup;
	private int catLevel;
	
	public int getCatCode() {
		return catCode;
	}
	
	public void setCatCode(int catCode) {
		this.catCode = catCode;
	}
	
	public String getCatName() {
		return catName;
	}
	
	public void setCatName(String catName) {
		this.catName = catName;
	}
	
	public int getCatGroup() {
		return catGroup;
	}
	
	public void setCatGroup(int catGroup) {
		this.catGroup = catGroup;
	}
	
	public int getCatLevel() {
		return catLevel;
	}
	
	public void setCatLevel(int catLevel) {
		this.catLevel = catLevel;
	}
	
}
